#include <iostream>
using namespace std;

int main()
{
    int arr[] = {4, 2, 7, 8, 1, 2, 5};
    int sz = 7;

    for (int i = sz - 1; i >= 0; i--)
    {
        cout << arr[i] << " ";
    }

    return 0;
}

